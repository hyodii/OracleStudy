    200만원 이상인 직원들의 수 조회.
    
SELECT T.월급분류,COUNT(*)
FROM
(
SELECT CASE WHEN BASICPAY + SUDANG>=2000000 THEN '200이상' 
            WHEN BASICPAY + SUDANG>=1000000 THEN '100이상'
            WHEN BASICPAY + SUDANG<1000000 THEN '100미만'
            ELSE '나머지값'

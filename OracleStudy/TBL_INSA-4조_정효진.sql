--94. 부서별 평균 급여를 조회하되, A, B, C 등급으로 나눠서 출력.
    200만원 초과 - A등급
    150~200만원  - B등급
    150만원 미만 - C등급

SELECT T.부서
     , CASE WHEN T.평균급여>=2500000 THEN 'A등급' 
            WHEN T.평균급여>=2000000 THEN 'B등급'
            ELSE 'C등급'
        END "등급"
FROM
(
    SELECT BUSEO"부서", AVG(NVL(BASICPAY,0)+NVL(SUDANG,0))"평균급여"
    FROM TBL_INSA
    GROUP BY BUSEO
)T;

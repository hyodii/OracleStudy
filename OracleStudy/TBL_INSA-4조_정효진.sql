--37. 전체인원수, 남자인원수, 여자인원수를 동시 조회.
SELECT CASE GROUPING(T.성별) WHEN 0 THEN T.성별 ELSE '전체인원수' END "성별분류"
    , COUNT(*)"인원수"
FROM
(
SELECT CASE WHEN SUBSTR(SSN,8,1) IN(1,3) THEN '남자인원수' 
            WHEN SUBSTR(SSN,8,1) IN(2,4) THEN '여자인원수' 
            ELSE '성별확인불가'
        END "성별"
FROM TBL_INSA
)T
